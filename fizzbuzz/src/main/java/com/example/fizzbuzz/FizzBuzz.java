package com.example.fizzbuzz;

public class FizzBuzz {
    public String Generate(int number) {
        if (number == 3) {
            return "Fizz";
        }
        if (number == 6) {
            return "Fizz";
        }
        if (number == 9) {
            return "Fizz";
        }
        return String.valueOf(number);
    }
}
